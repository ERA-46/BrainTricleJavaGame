    private void btnCreateAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateAccountActionPerformed
        // TODO add your handling code here:
       /**
        * When the create account button pressed, game will check for input errors,mismatch passwords and show alert to the player.
        * If the details are correct new account will create.
        */
        String Name;
        String Age;
        String Password;
        String ConPassword = txtConPassword.getText().trim();
        
        //setting user details from input to the user object
        User.setName(txtName.getText().trim());
        User.setAge(txtAge.getText().trim());
        User.setPassword(txtNewPassword.getText().trim());

        //getting details from the user object
        Name = User.getName();
        Age = User.getAge();
        Password = User.getPassword();
        
        //check for empty fields to prompt error message to fill 
        if(txtName.getText().equals("") || txtAge.getText().equals("")){
        
            JOptionPane.showMessageDialog(rootPane, "Please Fill the Remainning Fields! ");
        }
        
        //check weather the password and confirm password matchs
        if(!Password.equals(ConPassword) || txtNewPassword.getText().equals("") || txtConPassword.getText().equals("")){
        
            JOptionPane.showMessageDialog(rootPane, "Password didn't match! Please Try Again!");
            txtNewPassword.setText("");
            txtConPassword.setText("");
        }
        
        //check for invalid name inputs
        if(!(Pattern.matches("^[a-zA-Z\\s]{0,20}$", Name))){
        
            JOptionPane.showMessageDialog(rootPane, "Invalid Name! ");
            txtName.setText("");
        }
        
        //check for invalid age inputs
        if(!(Pattern.matches("^[0-9]{2}$", Age))){
        
            JOptionPane.showMessageDialog(rootPane, "Invalid Age! ");
            txtAge.setText("");
        }
        
        //create user account if all the fields are valid
        if(Password.equals(ConPassword) && Pattern.matches("^[a-zA-Z\\s]{0,20}$", Name) && Pattern.matches("^[0-9]{2}$", Age) && !Name.equals("") && !Age.equals("")){
        
            try {
                reg = LocateRegistry.getRegistry("localhost", 2000);
                ServiceInterface inter = (ServiceInterface) reg.lookup("RMIserver");

                //pass data to the addNewUser method in the ServiceInterface via RMI server
                inter.addNewUser(Name, Age, Password);
                
                txtName.setText("");
                txtAge.setText("");
                txtNewPassword.setText("");
                txtConPassword.setText("");
                
                System.out.println("User Added!");
                JOptionPane.showMessageDialog(rootPane, "User Added. Proceed to Login!!!");
                
                //dispose the current GUI and load the Login GUI
                this.setVisible(false);
                new Login().setVisible(true);
                
            } catch (NotBoundException | RemoteException ex) {
                System.out.println("Exception " + ex);
            }
        }

    }//GEN-LAST:event_btnCreateAccountActionPerformed
  
