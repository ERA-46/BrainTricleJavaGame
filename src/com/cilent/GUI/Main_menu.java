    private void txtAgeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAgeKeyReleased
        // TODO add your handling code here:
        
        /**
         * check for the age input and show an error text for invalid age input
         */
        String namePattern = "^[0-9]{0,2}$";
        Pattern patt = Pattern.compile(namePattern);
        Matcher match = patt.matcher(txtAge.getText());
        if (!match.matches()) {
            lblAgeError.setText("Incorrect Age...");
        } else {
            lblAgeError.setText(null);
        }
    }//GEN-LAST:event_txtAgeKeyReleased

